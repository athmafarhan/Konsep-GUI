    public void connectDatabase(){
        tblCustomerOutdoor.setModel(dataCustomer);
        Statement st;
        ResultSet rs;
        String viewJenisKamera = "select * from km_cust";
           try {
               st = new Koneksi().getCon().createStatement();
               rs = st.executeQuery(viewJenisKamera);
               int i = 0;
               while(rs.next()){
                   
                   CustomerOutdoor CO = new CustomerOutdoor(
                           rs.getInt("id"), 
                           rs.getString("nama"), 
                           rs.getString("no_hp"), 
                           rs.getString("alamat_rumah"), 
                           rs.getString("instagram"),
                           String.valueOf(rs.getInt("blacklist")));
                   ListCustOutdoor.add(CO);
                if (ListCustOutdoor.get(i).getStatus().equals("0")) {
                    ListCustOutdoor.get(i).setStatus("Verified");
                }
                else {
                    ListCustOutdoor.get(i).setStatus("Blacklist");
                }
                i++;
                   
                   Object []kolom = {
                       CO.getId(), 
                       CO.getNama(), 
                       CO.getNo_hp(), 
                       CO.getAlamat_rumah(), 
                       CO.getInstagram(),
                       CO.getStatus()};
                   
                   dataCustomer.addRow(kolom);
                   
                   }
               
               dataCustomer.fireTableDataChanged();
           }catch (Exception e){
                JOptionPane.showMessageDialog(null,"error :"+e.getMessage());
                e.printStackTrace();
                } 
        
            
    }
        
    
    
    public void updateTableAccesories() {
        int rowCount = dataCustomer.getRowCount()-1;
        
        for (int i = rowCount; i >= 0; i--) {
            dataCustomer.removeRow(i);
        }
        for (CustomerOutdoor CO : ListCustOutdoor) {
            Object []kolom = {
                       CO.getId(), 
                       CO.getNama(), 
                       CO.getNo_hp(), 
                       CO.getAlamat_rumah(), 
                       CO.getInstagram(),
                       CO.getStatus()};
            dataCustomer.addRow(kolom);
        }

        dataCustomer.fireTableDataChanged();
    }
