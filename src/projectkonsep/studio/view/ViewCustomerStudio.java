    public void connectDatabase(){
        tblCustomerStudio.setModel(dataCustomer);
        Statement st;
        ResultSet rs;
        String viewJenisKamera = "select * from st_cust";
           try {
               st = new Koneksi().getCon().createStatement();
               rs = st.executeQuery(viewJenisKamera);
               int i = 0;
               while(rs.next()){
                   
                   CustomerStudio CO = new CustomerStudio(
                           rs.getInt("id"), 
                           rs.getString("nama"), 
                           rs.getString("no_hp"), 
                           rs.getString("alamat_rumah"), 
                           rs.getString("instagram"),
                           String.valueOf(rs.getInt("blacklist")));
                   ListCustStudio.add(CO);
                if (ListCustStudio.get(i).getStatus().equals("0")||ListCustStudio.get(i).getStatus().equals("Verified")) {
                    ListCustStudio.get(i).setStatus("Verified");
                }
                else {
                    ListCustStudio.get(i).setStatus("Blacklist");
                }
                i++;
                   
                   Object []kolom = {
                       CO.getId(), 
                       CO.getNama(), 
                       CO.getNo_hp(), 
                       CO.getAlamat(), 
                       CO.getInstagram(),
                       CO.getStatus()};
                   
                   dataCustomer.addRow(kolom);
                   
                   }
               
               dataCustomer.fireTableDataChanged();
           }catch (Exception e){
                JOptionPane.showMessageDialog(null,"error :"+e.getMessage());
                e.printStackTrace();
                } 
        
            
    }
        
    
    
    public void updateTableAccesories() {
        int rowCount = dataCustomer.getRowCount()-1;
        
        for (int i = rowCount; i >= 0; i--) {
            dataCustomer.removeRow(i);
        }
        for (CustomerStudio CO : ListCustStudio) {
            Object []kolom = {
                       CO.getId(), 
                       CO.getNama(), 
                       CO.getNo_hp(), 
                       CO.getAlamat(), 
                       CO.getInstagram(),
                       CO.getStatus()};
            dataCustomer.addRow(kolom);
        }

        dataCustomer.fireTableDataChanged();
    }
