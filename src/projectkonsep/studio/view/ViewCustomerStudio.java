    private void showData() {
        CustomerStudio CO = ListCustStudio.get(index);
        txtID.setText(String.valueOf(CO.getId()));
        txtNama.setText(CO.getNama());
        txtNo_Hp.setText(CO.getNo_hp());
        txtAlamat.setText(CO.getAlamat());
        txtInstagram.setText(CO.getInstagram());
        jComboBox1.setSelectedItem(CO.getStatus());
        if (ListCustStudio.get(index).getStatus().equals("0")||ListCustStudio.get(index).getStatus().equals("Verified")) {
                    ListCustStudio.get(index).setStatus("Verified");
        }
        else {
            ListCustStudio.get(index).setStatus("Blacklist");
        }
    }
